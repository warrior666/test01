CREATE TABLE meteorological_data(
    id BIGINT NOT NULL PRIMARY KEY,
    local_date DATE NOT NULL,
    latitude DECIMAL(4,2) NOT NULL,
    longitude DECIMAL(5,2) NOT NULL,
    city VARCHAR(300) NOT NULL,
    state VARCHAR(300) NOT NULL
);

